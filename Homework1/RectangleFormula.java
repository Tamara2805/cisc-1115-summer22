public class RectangleFormula {

  //assigning variables
  static int width = 6;
  static int length = 7;

  //equations
  static int area = width*length;
  static int perimeter = (2*width)+(2*length);

  //ending statement
  public static void main(String[] args) {

  System.out.print("When one side is ");
  System.out.print(width);
  System.out.print(" and the other side is ");
  System.out.print(length);
  System.out.print(", the area is ");
  System.out.print(area);
  System.out.print(" and the perimeter is ");
  System.out.print(perimeter);
  System.out.println(".");
}
}
